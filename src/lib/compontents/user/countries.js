/** @param {string} countryCode */
function getFlagEmoji(countryCode) {
	if (countryCode.length !== 2) return '';
	return countryCode
		.toUpperCase()
		.split('')
		.map((char) => String.fromCodePoint(127397 + char.charCodeAt(0)))
		.join('');
}

